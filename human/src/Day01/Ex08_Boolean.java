package Day01;

public class Ex08_Boolean {

	public static void main(String[] args) {
		boolean on = false;
		
		System.out.println("on : " + on);
		
		if( on ) {
			System.out.println("전원 ON");  // if의 값이 true일 때 실행
		} else {
		System.out.println("전원 OFF");   // if의 값이 false일 때 실행
		}
		
	}

}
